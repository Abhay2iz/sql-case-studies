SET Product_Type = 'Tea'
WHERE ProductId = 1;

SELECT * FROM Product WHERE ProductID = 1;

ROLLBACK TRANSACTION;

SELECT * FROM Product WHERE ProductId = 1; -- confirms reverted to 'Coffee'


